package de.samply.share.client.control;

public class ExportFileGeneratorException extends Exception {

  public ExportFileGeneratorException() {
  }

  public ExportFileGeneratorException(String message) {
    super(message);
  }

  public ExportFileGeneratorException(String message, Throwable cause) {
    super(message, cause);
  }

  public ExportFileGeneratorException(Throwable cause) {
    super(cause);
  }

}
