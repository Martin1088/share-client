package de.samply.share.client.quality.report.views.fromto;

public class ViewFromTo {

  private String from;
  private String to;

  public ViewFromTo(String from, String to) {
    this.from = from;
    this.to = to;
  }

  public String getFrom() {
    return from;
  }

  public String getTo() {
    return to;
  }

}
