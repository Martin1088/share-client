package de.samply.share.client.quality.report.chainlinks;

public class ChainLinkException extends Exception {


  public ChainLinkException(String message) {
    super(message);
  }

  public ChainLinkException(Throwable throwable) {
    super(throwable);
  }

  public ChainLinkException(String message, Throwable throwable) {
    super(message, throwable);
  }

}
